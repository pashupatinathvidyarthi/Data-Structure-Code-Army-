//To print number from 1 to 10
#include<iostream>
using namespace std;
int main()
{
    int i=1;
    while(i<=10)
    {
        cout<<i<<" ";
        i++;
    }
    return 0;
}


//To print table of 8
#include<iostream>
using namespace std;
int main()
{
    int i=8;
    int j=1;
    while(j<=10)
    {
        cout<<i<<" * "<<j<<" = "<<i*j<<endl;
        j++;
    }
    return 0;
}