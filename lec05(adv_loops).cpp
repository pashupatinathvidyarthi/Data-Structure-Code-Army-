//Print numbers from 100 to 200
#include<iostream>
using namespace std;
int main()
{
    for(int i=100;i<=200;i++)
    cout<<i<<"\n";
    return 0;
}


//Print character from a to z
#include<iostream>
using namespace std;
int main()
{
    for(char c='a';c<='z';c++)
    cout<<c<<"\n";
    return 0;
}